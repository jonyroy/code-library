int mem[1000][1000];
int dp(char s1[],char s2[])
{
    int n,m,i,j;
    n=strlen(s1);
    m=strlen(s2);
    mem[0][0]=0;
    for(i=1;i<=n;i++)
    mem[i][0]=i;
    for(j=1;j<=m;j++)
    mem[0][j]=j;
    for(i=1;i<=n;i++)
    for(j=1;j<=m;j++)
    if(s1[i]==s2[j])
    {
        mem[i][j]=MIN(mem[i-1][j-1],mem[i-1][j]+1);
        mem[i][j]=MIN(mem[i][j],mem[i][j-1]+1);
    }
    else
    {
        mem[i][j]=MIN(mem[i-1][j-1]+1,mem[i-1][j]+1);
        mem[i][j]=MIN(mem[i][j],mem[i][j-1]+1);
    }
    return mem[n][m];
}

